package com.oraclejava.springcore2;

public class Person {
	
	private String name;
	private int age;
	private PersonAddress addr;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public PersonAddress getAddr() {
		return addr;
	}
	public void setAddr(PersonAddress addr) {
		this.addr = addr;
	}
	
	
	
}
