package com.oraclejava.springcore3;

// 사람 클래스
public class Person {
	private String name;  // 이름
	private int age; // 나이
	
	// get, set
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
}
