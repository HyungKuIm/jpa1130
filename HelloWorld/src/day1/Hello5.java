package day1;

public class Hello5 {

	public static void main(String[] args) {
		int x = 10;
		int y = 4;  //실수와 정수의 계산은 자동 형변환됨
		
		// 수학 연산자
		System.out.println(x + y); // + 더하기
		System.out.println(x - y); // - 빼기
		System.out.println(x * y); // + 곱하기
		System.out.println(x / (double)y); // / 나누기
		System.out.println(x % y); // % 나머지
		
		

	}

}
